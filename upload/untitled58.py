train_size = int(len(X_seq) * 0.8)
X_train_seq, X_val_seq = X_seq[:train_size], X_seq[train_size:]
y_train_seq, y_val_seq = y_seq[:train_size], y_seq[train_size:]

# 2. Refined Bahdanau Attention Mechanism
class BahdanauAttention(layers.Layer):
    def __init__(self, units):
        super(BahdanauAttention, self).__init__()
        self.W1 = layers.Dense(units)
        self.W2 = layers.Dense(units)
        self.V = layers.Dense(1)

    def call(self, features):
        # score shape: (batch_size, max_length, units)
        score = tf.nn.tanh(self.W1(features))
        # attention_weights shape: (batch_size, max_length, 1)
        attention_weights = tf.nn.softmax(self.V(score), axis=1)
        # context_vector shape after sum: (batch_size, hidden_size)
        context_vector = attention_weights * features
        context_vector = tf.reduce_sum(context_vector, axis=1)
        return context_vector

# 3. Build Model
inputs = layers.Input(shape=(30, 1))
lstm_out = layers.LSTM(64, return_sequences=True)(inputs)
context_vector = BahdanauAttention(64)(lstm_out)
dense_out = layers.Dense(32, activation='relu')(context_vector)
outputs = layers.Dense(1)(dense_out)

model_attn_refined = Model(inputs, outputs)
model_attn_refined.compile(optimizer='adam', loss='mse', metrics=['mae'])

